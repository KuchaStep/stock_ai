# 重複コードを除去
seen = set()
unique_stocks = []
for s in stocks:
    if s[0] not in seen:
        seen.add(s[0])
        unique_stocks.append(s)

df = pd.DataFrame(unique_stocks, columns=["code", "name"])

conn = sqlite3.connect("database/stock.db")
df.to_sql("stocks", conn, if_exists="replace", index=False)
conn.close()

